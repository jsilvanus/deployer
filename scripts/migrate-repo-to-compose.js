#!/usr/bin/env node
/**
 * scripts/migrate-repo-to-compose.js
 * Usage: node scripts/migrate-repo-to-compose.js <path-to-app>
 *
 * - No external dependencies (Node.js builtin modules only)
 * - Writes `docker-compose.generated.yml` and `<service>.env` in the current working directory
 */

const fs = require('fs');
const path = require('path');

function readJson(p) {
  try { return JSON.parse(fs.readFileSync(p, 'utf8')); }
  catch (e) { return null; }
}

function findPortFromEnvText(envText) {
  const m = envText.match(/(^|\n)\s*PORT\s*=\s*(\d+)/);
  return m ? m[2] : null;
}

function normalizeRelPath(from, to) {
  let rel = path.relative(from, to) || '.';
  rel = rel.split(path.sep).join('/');
  if (!rel.startsWith('.') && !rel.startsWith('/')) rel = './' + rel;
  return rel;
}

function slugName(name) {
  return name.replace(/[^a-zA-Z0-9._-]/g, '-').toLowerCase();
}

function main() {
  const args = process.argv.slice(2);
  if (!args[0]) {
    console.error('Usage: node scripts/migrate-repo-to-compose.js <path-to-app>');
    process.exit(2);
  }

  const target = path.resolve(process.cwd(), args[0]);
  if (!fs.existsSync(target) || !fs.statSync(target).isDirectory()) {
    console.error('Path not found or not a directory:', target);
    process.exit(3);
  }

  const pkg = readJson(path.join(target, 'package.json')) || {};
  const serviceName = slugName(path.basename(target) || (pkg.name || 'app'));
  let port = null;
  if (typeof pkg.port === 'number' || typeof pkg.port === 'string') port = String(pkg.port);
  else if (pkg.config && (pkg.config.port || pkg.config.PORT)) port = String(pkg.config.port || pkg.config.PORT);
  else if (Array.isArray(pkg.exposedPorts) && pkg.exposedPorts.length) port = String(pkg.exposedPorts[0]);
  const envPath = path.join(target, '.env');
  let envText = null;
  if (fs.existsSync(envPath)) {
    envText = fs.readFileSync(envPath, 'utf8');
    const p = findPortFromEnvText(envText);
    if (p) port = p;
  }
  if (!port) port = '3000';

  const outEnvName = `${serviceName}.env`;
  const outEnvPath = path.join(process.cwd(), outEnvName);

  if (envText) {
    fs.writeFileSync(outEnvPath, envText.replace(/\r\n/g, '\n').trim() + '\n', { encoding: 'utf8' });
  } else {
    const lines = [
      `# generated by migrate-repo-to-compose.js`,
      `PORT=${port}`,
      `NODE_ENV=production`,
    ];
    fs.writeFileSync(outEnvPath, lines.join('\n') + '\n', { encoding: 'utf8' });
  }

  const buildContext = normalizeRelPath(process.cwd(), target);

  const compose = [
    "version: '3.8'",
    'services:',
    `  ${serviceName}:`,
    '    build:',
    `      context: ${buildContext}`,
    '      dockerfile: deployer.dockerfile',
    `    container_name: ${serviceName}`,
    '    ports:',
    `      - "${port}:${port}"`,
    '    env_file:',
    `      - ./${outEnvName}`,
    '    restart: unless-stopped',
    '    labels:',
    `      - "com.deployer.generated=true"`,
  ].join('\n') + '\n';

  const outComposePath = path.join(process.cwd(), 'docker-compose.generated.yml');
  fs.writeFileSync(outComposePath, compose, { encoding: 'utf8' });

  console.log('Generated:', outComposePath);
  console.log('Env file:', outEnvPath);
  console.log('Service:', serviceName);
  console.log('Build context:', buildContext);
  console.log('Port:', port);
}

main();
